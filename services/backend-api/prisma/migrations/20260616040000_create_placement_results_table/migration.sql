-- P4-023: Create Placement Results Migration
-- Branch: phase4/P4-023-placement-results-migration
-- Dependency: P4-014 (Placement Result Contract), P4-022 (Placement Answers Migration)
-- Scope: Placement results table only. No AIM Engine runtime.

-- ============================================================
-- Table: placement_results
-- ============================================================

CREATE TABLE placement_results (
    id                      UUID            PRIMARY KEY DEFAULT gen_random_uuid(),
    placement_attempt_id    UUID            NOT NULL UNIQUE REFERENCES placement_attempts (id) ON DELETE CASCADE,
    student_id              UUID            NOT NULL,
    estimated_level         TEXT            NOT NULL
                                            CHECK (estimated_level IN (
                                                'beginner',
                                                'elementary',
                                                'intermediate',
                                                'upper_intermediate',
                                                'advanced'
                                            )),
    skill_mastery_map       JSONB           NOT NULL DEFAULT '{}',
    weakness_map            JSONB           NOT NULL DEFAULT '{}',
    initial_path_id         UUID            NULL,
    created_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE placement_results IS
    'Stores backend-computed placement results after attempt completion. One result per attempt.';

COMMENT ON COLUMN placement_results.id IS
    'Primary key — UUID generated by backend.';

COMMENT ON COLUMN placement_results.placement_attempt_id IS
    'Foreign key to placement_attempts — unique, one result per attempt.';

COMMENT ON COLUMN placement_results.student_id IS
    'Foreign key to students table — denormalized for fast lookup.';

COMMENT ON COLUMN placement_results.estimated_level IS
    'Backend-computed placement level. Flutter must never calculate or infer this value.';

COMMENT ON COLUMN placement_results.skill_mastery_map IS
    'JSONB object with per-skill mastery data. Computed by backend per P4-014 contract.';

COMMENT ON COLUMN placement_results.weakness_map IS
    'JSONB object listing skill weaknesses by priority. Computed by backend per P4-014 contract.';

COMMENT ON COLUMN placement_results.initial_path_id IS
    'UUID of the learning path assigned by backend based on level and weaknesses. Null until assigned.';

-- ============================================================
-- Indexes
-- ============================================================

CREATE INDEX placement_results_attempt_id_idx
    ON placement_results (placement_attempt_id);

CREATE INDEX placement_results_student_id_idx
    ON placement_results (student_id);

CREATE INDEX placement_results_estimated_level_idx
    ON placement_results (estimated_level);

CREATE INDEX placement_results_initial_path_id_idx
    ON placement_results (initial_path_id);